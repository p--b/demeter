<h2>How do I receive my tickets?</h2>
<p>Tickets are sent to you by e-mail for you to print and bring with you. This eliminates any delivery costs!</p>

<h2>How do I pay? Is it secure?</h2>
<p>We take Credit and Debit cards through our payment provider, Stripe. The entire booking system is protected by modern encryption.</p>

